package org.sakaiproject.nakamura.api.user;

import java.util.Map;

public class DefaultSakaiPerson implements SakaiPerson {
  public DefaultSakaiPerson(String id, String firstName, String lastName, String email, Map<String, Object> properties) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.email = email;
    this.properties = properties;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getFirstName() {
    return firstName;
  }

  public void setFirstName(String firstName) {
    this.firstName = firstName;
  }

  public String getLastName() {
    return lastName;
  }

  public void setLastName(String lastName) {
    this.lastName = lastName;
  }

  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }

  public Map<String, Object> getProperties() {
    return properties;
  }

  public void setProperties(Map<String, Object> properties) {
    this.properties = properties;
  }

  private String id;
  private String firstName;
  private String lastName;
  private String email;
  private Map<String, Object> properties;
}
